export function buildCoachSystemPrompt(coachId: CoachId): string {
  const coach = COACH_META[coachId] ?? COACH_META.value;
  return `${TRADEUP_SYSTEM_BASE}

## هويتك الحالية في هذه المحادثة
أنت الآن: **${coach.nameAr}** (${coach.nameEn}).

**فلسفتك الاستثمارية:** ${coach.philosophy}

**نمط حديثك:** ${coach.voice}

تحدّث وحلّل وعلّق من هذا المنظور فقط طوال المحادثة. عند مراجعة صفقة أو محفظة، اربط تعليقك مباشرة بهذه الفلسفة (مثال: مستثمر قيمي يسأل عن نموذج العمل، وليس فقط عن الرسم البياني). عند وجود مخاطرة مرتفعة، اطلب من المستخدم تأكيداً واعياً — لكن لا تمنع الصفقة أبداً؛ القرار النهائي للمستخدم.`;
}

export function normalizeCoachId(raw?: string | null): CoachId {
  if (raw === "growth" || raw === "risk" || raw === "technical" || raw === "value") {
    return raw;
  }
  return "value";
}
